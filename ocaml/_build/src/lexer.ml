let create_char_token c =
  match c with
  | '=' -> EQ
  | '-' -> SUB
  | '+' -> ADD
  | '/' -> DIV
  | '*' -> MUL
  | '(' -> LPAREN
  | ')' -> RPAREN
  | _   -> failwith "Expected single char, found string."

let create_str_token str =
  match str with
  | "->"   -> RARROW
  | "<="   -> LEQ
  | _      -> failwith "Expected nonalphabetic string."

let create_alpha_token str =
  match str with
  | "true"  -> BOOL true
  | "false" -> BOOL false
  | "let"   -> LET
  | "in"    -> IN
  | "fun"   -> FUN
  | "fix"   -> FIX
  | "if"    -> IF
  | "then"  -> THEN
  | "else"  -> ELSE
  | _       -> VAR (str)

