let create_char_token c =
  match c with
  | ';' -> SEMI
  | ',' -> COMMA
  | '=' -> EQ
  | '<' -> LANGL
  | '>' -> RANGL
  | '-' -> SUB
  | '+' -> ADD
  | '/' -> DIV
  | '*' -> MUL
  | ':' -> COLON
  | '!' -> BANG
  | '(' -> LPAREN
  | ')' -> RPAREN
  | _   -> failwith "Expected single char, found string."

let create_str_token str =
  match str with
  | ":="   -> ASS
  | "<="   -> LEQ
  | "->"   -> RARROW
  | _      -> failwith "Expected nonalphabetic string."

let create_alpha_token str =
  match str with
  | "true"  -> BOOL true
  | "false" -> BOOL false
  | "int"   -> TINT
  | "bool"  -> TBOOL
  | "unit"  -> TUNIT
  | "ref"   -> REF
  | "let"   -> LET
  | "in"    -> IN
  | "fun"   -> FUN
  | "fix"   -> FIX
  | "if"    -> IF
  | "then"  -> THEN
  | "else"  -> ELSE
  | "fst"   -> FIRST
  | "snd"   -> SECOND
  | "while" -> WHILE
  | "do"    -> DO
  | "end"   -> END
  | _       -> VAR (str)

